    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[          //Validation before storing posts 
            'name'=>'required|max:80',
            'description'=>'required',
            ]);
        $post = new Post();                 //create new post object
        $post->id = $request->id;         //requesting and storing the supplied info sent from the form
        $post->name = $request->name;
        $post->description = $request->description;
        $post->save(); 
        return redirect()->back()->with('success', 'Post successful'); //Redirect back to the previous page with Success note.
    }

